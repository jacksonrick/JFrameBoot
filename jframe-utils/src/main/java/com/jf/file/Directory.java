package com.jf.file;

/**
 * 目录实体类
 * 
 * @author rick
 *
 */
public class Directory {

	private String name;

	private String path;

	private String type;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
